# Rectangle Area Calculator

# This program calculates the area of a rectangle given its length and width.
length = float(input("Enter the length of the rectangle: "))  # Input length
width = float(input("Enter the width of the rectangle: "))    # Input width
area = length * width  # Calculate area

print(f"The area of the rectangle is: {area}")  # Output the area
# The area is calculated using the formula: Area = Length * Width